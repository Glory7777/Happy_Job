package kr.happyjob.study.dashboard.model;

public class ProductSalesDataModel {
	
	private String pro_cd; // 상품번호
	private String pro_nm; // 상품명
	private String total_sales; // 총판매액
	
	public String getPro_cd() {
		return pro_cd;
	}
	public void setPro_cd(String pro_cd) {
		this.pro_cd = pro_cd;
	}
	public String getPro_nm() {
		return pro_nm;
	}
	public void setPro_nm(String pro_nm) {
		this.pro_nm = pro_nm;
	}
	
	public String getTotal_sales() {
		return total_sales;
	}
	public void setTotal_sales(String total_sales) {
		this.total_sales = total_sales;
	}

	

}
