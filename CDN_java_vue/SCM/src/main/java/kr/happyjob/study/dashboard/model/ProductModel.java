package kr.happyjob.study.dashboard.model;

public class ProductModel {
	
	private String pro_cd;
	private String pro_nm;
	private String pro_model_nm;
	private int pro_sup_price;
	private String pro_dt;
	private String logic_path;
	
	
	public String getPro_cd() {
		return pro_cd;
	}
	public void setPro_cd(String pro_cd) {
		this.pro_cd = pro_cd;
	}
	public String getPro_nm() {
		return pro_nm;
	}
	public void setPro_nm(String pro_nm) {
		this.pro_nm = pro_nm;
	}
	public String getPro_model_nm() {
		return pro_model_nm;
	}
	public void setPro_model_nm(String pro_model_nm) {
		this.pro_model_nm = pro_model_nm;
	}
	public int getPro_sup_price() {
		return pro_sup_price;
	}
	public void setPro_sup_price(int pro_sup_price) {
		this.pro_sup_price = pro_sup_price;
	}
	public String getPro_dt() {
		return pro_dt;
	}
	public void setPro_dt(String pro_dt) {
		this.pro_dt = pro_dt;
	}
	public String getLogic_path() {
		return logic_path;
	}
	public void setLogic_path(String logic_path) {
		this.logic_path = logic_path;
	}
	
	

    
}